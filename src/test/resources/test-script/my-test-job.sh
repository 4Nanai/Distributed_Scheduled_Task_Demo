#!/bin/bash

echo "=================================================="
echo "脚本开始执行..."
echo "当前分片: $SHARD_ITEM"
echo "=================================================="

# 根据SHARD_ITEM输出不同内容
case $SHARD_ITEM in
    0)
        echo "分片0 - 输出当前年月日:"
        echo "年月日: $(date '+%Y-%m-%d')"
        ;;
    1)
        echo "分片1 - 输出当前时分秒:"
        echo "时分秒: $(date '+%H:%M:%S')"
        ;;
    2)
        echo "分片2 - 输出时间戳:"
        echo "时间戳: $(date +%s)"
        ;;
    *)
        echo "未知分片: $SHARD_ITEM"
        echo "默认输出完整时间: $(date '+%Y-%m-%d %H:%M:%S')"
        ;;
esac

echo "=================================================="
echo "脚本执行完成！"
echo "=================================================="
